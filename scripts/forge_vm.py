def parse_constructor_fields(ml_text: str) -> dict[str, list[str]]:
    """Parse vm_instruction constructor names and their field types."""
    ctors: dict[str, list[str]] = {}
    in_type = False
    for line in ml_text.splitlines():
        if not in_type:
            if re.match(r"\s*type\s+vm_instruction\s*=\s*$", line):
                in_type = True
            continue
        stripped = line.strip()
        if not stripped:
            if ctors:
                break
            continue
        if stripped.startswith("type ") and ctors:
            break
        if stripped.startswith("let ") and ctors:
            break
        m = re.match(r"\|\s*(\w+)(?:\s+of\s+(.+))?$", stripped)
        if m:
            name = m.group(1)
            # Normalize OCaml extraction prefix: Coq_instr_X -> Instr_X
            if name.startswith("Coq_instr_"):
                name = "Instr_" + name[len("Coq_instr_"):]
            fields_str = m.group(2) or ""
            if fields_str:
                fields = [f.strip() for f in fields_str.split("*")]
            else:
                fields = []
            ctors[name] = fields
    return ctors


def parse_cert_setters(ml_text: str) -> set[str]:
    """Parse is_cert_setterb to find which constructors are cert-setters."""
    setters: set[str] = set()
    in_func = False
    for line in ml_text.splitlines():
        if "let is_cert_setterb" in line:
            in_func = True
            continue
        if in_func:
            m = re.match(r"\s*\|\s*(\w+)\s+.*->\s*true", line)
            if m:
                name = m.group(1)
                if name.startswith("Coq_instr_"):
                    name = "Instr_" + name[len("Coq_instr_"):]
                setters.add(name)
            if "| _ -> false" in line:
                break
    return setters


def extract_constants(ml_text: str) -> dict[str, int]:
    """Extract named integer constants from preprocessed OCaml."""
    constants: dict[str, int] = {}
    for name in ["rEG_COUNT", "mEM_SIZE", "mAX_MODULES", "tENSOR_SIZE"]:
        # Try plain integer (post Peano-collapse)
        m = re.search(rf"let\s+{name}\s*=\s*(\d+)", ml_text)
        if m:
            constants[name] = int(m.group(1))
            continue
        # Try of_num_uint(UIntDecimal(D...Nil)) format (Coq 8.18+ extraction)
        m = re.search(
            rf"let\s+{name}\s*=\s*\n?\s*of_num_uint\s+\(UIntDecimal\s+\((.+?Nil)\)\)",
            ml_text, re.DOTALL,
        )
        if m:
            digits = re.findall(r'D(\d)', m.group(1))
            if digits:
                constants[name] = int(''.join(digits))
    return constants


