def generate_full_vm(ml_text: str) -> str:
    """Generate the complete thielecpu/vm.py from OCaml IR text."""
    preprocessed = collapse_peano_towers(ml_text)

    constructors = parse_constructor_fields(preprocessed)
    if not constructors:
        raise SystemExit("forge_vm.py: could not parse vm_instruction constructors")

    n_opcodes = len(constructors)
    print(f"forge_vm.py: found {n_opcodes} opcodes in OCaml IR")

    unknown = set(constructors.keys()) - set(CONSTRUCTOR_FIELD_MAP.keys())
    if unknown:
        raise SystemExit(
            f"forge_vm.py: unknown constructors in OCaml IR: {', '.join(sorted(unknown))}\n"
            f"Add them to CONSTRUCTOR_FIELD_MAP in forge_vm.py"
        )

    missing = set(CONSTRUCTOR_FIELD_MAP.keys()) - set(constructors.keys())
    if missing:
        raise SystemExit(
            f"forge_vm.py: constructors in CONSTRUCTOR_FIELD_MAP not found in OCaml IR: "
            f"{', '.join(sorted(missing))}\n"
            f"Update CONSTRUCTOR_FIELD_MAP in forge_vm.py"
        )

    cert_setters = parse_cert_setters(preprocessed)
    constants = extract_constants(preprocessed)

    print(f"forge_vm.py: cert_setters = {sorted(ctor_to_opname(c) for c in cert_setters if c.startswith('Coq_instr_'))}")

    sections = [
        HEADER,
        generate_constants(constants),
        generate_types(),
        generate_cert_setters(cert_setters),
        generate_instr_dict_to_text(),
        generate_runner_and_vm(),
    ]

    return "\n".join(sections)


def main() -> int:
    ap = argparse.ArgumentParser(
        description="Generate thielecpu/vm.py OCaml-runner wrapper from Coq-extracted OCaml"
    )
    ap.add_argument("--input", required=True, help="Path to build/thiele_core.ml")
    ap.add_argument("--output", required=True, help="Path to thielecpu/vm.py")
    ap.add_argument(
        "--check-only",
        action="store_true",
        help="Don't write; verify committed file matches regeneration",
    )
    args = ap.parse_args()

    ml_path = Path(args.input)
    out_path = Path(args.output)

    ml_text = ml_path.read_text(encoding="utf-8", errors="replace")
    generated = generate_full_vm(ml_text)

    if args.check_only:
        if not out_path.exists():
            raise SystemExit(f"forge_vm.py: --check-only but {out_path} does not exist")
        existing = out_path.read_text(encoding="utf-8")
        if existing != generated:
            raise SystemExit(
                f"forge_vm.py: {out_path} is stale — regenerate with:\n"
                f"  python3 scripts/forge_vm.py --input {args.input} --output {args.output}"
            )
        print(f"forge_vm.py: {out_path} is up to date")
        return 0

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(generated, encoding="utf-8")
    print(f"forge_vm.py: wrote {out_path} ({len(generated)} bytes)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
