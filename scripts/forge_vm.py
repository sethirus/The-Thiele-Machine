def vm_run(state: VMState, program: List[Dict[str, Any]], max_steps: int = 10000) -> VMState:
    """Execute a list of instruction dicts starting from state.

    Delegates entirely to the OCaml extracted VM binary.
    No opcode semantics are implemented here.
    """
    prog_lines = [instr_dict_to_text(i) for i in program]
    return _call_runner(state, prog_lines, max_steps)


def vm_apply_unsafe(state: VMState, instr: Dict[str, Any]) -> VMState:
    """Apply a single instruction to state.

    Normalizes vm_pc to 0 so the OCaml runner executes the instruction
    at position 0 of a one-instruction program.
    """
    s0 = dataclasses.replace(state, vm_pc=0)
    return vm_run(s0, [instr], max_steps=2)


def vm_apply_nofi(state: VMState, instr: Dict[str, Any]) -> VMState:
    """Apply with NoFI enforcement.

    NoFI is enforced by the OCaml binary (CertCheck + SimulationProof).
    This function delegates entirely to the extracted runner.
    """
    return vm_apply_unsafe(state, instr)


def vm_apply_runtime(state: VMState, instr: Dict[str, Any]) -> VMState:
    """Runtime-safe apply (same as vm_apply_nofi)."""
    return vm_apply_nofi(state, instr)


def vm_apply(state: VMState, instr: Dict[str, Any]) -> VMState:
    """Apply a single instruction (alias for vm_apply_unsafe)."""
    return vm_apply_unsafe(state, instr)


