def generate_cert_setters(cert_setters: set[str]) -> str:
    """Generate _CERT_SETTERS from parsed is_cert_setterb arms."""
    opnames = sorted(ctor_to_opname(c) for c in cert_setters if c.startswith("Instr_"))
    items = ", ".join(f'"{n}"' for n in opnames)
    return f'''\
# ---------------------------------------------------------------------------
# Cert-setter opcodes (from is_cert_setterb in thiele_core.ml)
# These require positive cost (NoFI enforcement).
# ---------------------------------------------------------------------------
_CERT_SETTERS: frozenset = frozenset({{{items}}})
'''


def _fmt_list_field(val: Any) -> str:
    """Format a region/list field as {v1,v2,...}."""
    if not val:
        return "{}"
    return "{" + ",".join(str(x) for x in val) + "}"


