def collapse_peano_towers(text: str) -> str:
    """Collapse (fun x -> x + 1) ... 0 towers to integer literals."""
    prev = ""
    while prev != text:
        prev = text
        # Match (fun x -> x + 1) followed by 0 with any whitespace between
        text = re.sub(r"\(fun x -> x \+ 1\)\s+0", "1", text)
        # Strip redundant parens around bare integers: (123) -> 123
        text = re.sub(r"\((\d+)\)", r"\1", text)
        text = re.sub(
            r"\(fun x -> x \+ 1\)\s+(\d+)",
            lambda m: str(int(m.group(1)) + 1),
            text,
        )
    return text


