def _compute_state_mac(json_body: str) -> str:
    # FNV-1a 64-bit keyed hash for state integrity (tamper detection, not cryptographic).
    s = _STATE_INTEGRITY_SALT + json_body
    h = _FNV_OFFSET_BASIS
    for c in s:
        h = (h ^ ord(c)) & _MASK64
        h = (h * _FNV_PRIME) & _MASK64
    return "%016x" % h


