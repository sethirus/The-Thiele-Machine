"""Foundry loop-closure: generated Python surface matches Coq opcode definitions.

Validates that forge.py-generated Python tables (COQ_TAG_TO_MNEMONIC,
COQ_TAG_TO_OPCODE_BYTE) are internally consistent and match the opcode
definitions in ThieleTypes.v (the Coq source of truth for opcode encodings).
"""

from __future__ import annotations

import pathlib
import re

from thielecpu.generated import generated_core


ROOT = pathlib.Path(__file__).resolve().parents[1]


def _parse_coq_opcodes(path: pathlib.Path) -> dict[str, int]:
    """Parse OP_* definitions from ThieleTypes.v.

    Looks for patterns like:
        Definition OP_PNEW : word OpcodeSz := WO~0~0~0~0~0~0~0~0.
    """
    pattern = re.compile(
        r'Definition\s+OP_([A-Z_]+)\s*.*?:=\s*WO((?:~[01])+)\.'
    )
    out: dict[str, int] = {}
    for line in path.read_text(encoding="utf-8", errors="replace").splitlines():
        match = pattern.search(line)
        if match:
            name = match.group(1)
            bits = match.group(2).replace("~", "")
            out[name] = int(bits, 2)
    return out


def test_foundry_internal_consistency() -> None:
    """COQ_TAG_TO_MNEMONIC and COQ_TAG_TO_OPCODE_BYTE cover all tags."""
    assert set(generated_core.COQ_TAG_TO_MNEMONIC) == set(generated_core.COQ_INSTRUCTION_TAGS)
    assert set(generated_core.COQ_TAG_TO_OPCODE_BYTE) == set(generated_core.COQ_INSTRUCTION_TAGS)
    generated_core.sanity_check()


def test_foundry_matches_coq_thiele_types() -> None:
    """Generated Python opcode bytes match ThieleTypes.v OP_* definitions."""
    thiele_types = ROOT / "coq" / "kami_hw" / "ThieleTypes.v"
    if not thiele_types.exists():
        import pytest
        pytest.skip("ThieleTypes.v not found")

    coq_opcodes = _parse_coq_opcodes(thiele_types)
    if not coq_opcodes:
        import pytest
        pytest.skip("No OP_* definitions found in ThieleTypes.v")

    # Build Python mnemonic → byte map
    py_opcodes = generated_core.MNEMONIC_TO_OPCODE_BYTE

    # Every Coq opcode should match the Python-generated byte
    for name, coq_byte in coq_opcodes.items():
        assert name in py_opcodes, (
            f"Coq OP_{name} not found in generated Python MNEMONIC_TO_OPCODE_BYTE"
        )
        assert py_opcodes[name] == coq_byte, (
            f"OP_{name}: Coq={coq_byte:#04x} vs Python={py_opcodes[name]:#04x}"
        )
