def _kernel_v(name: str) -> Path:
    flat = COQ / "kernel" / name
    if flat.exists():
        return flat
    kernel_root = COQ / "kernel"
    if kernel_root.exists():
        for p in kernel_root.rglob(name):
            return p
    return flat
