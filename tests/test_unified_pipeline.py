def _run_trace(trace_lines, timeout=10):
    """Write a trace file, run the extracted VM runner, return parsed JSON."""
    with tempfile.NamedTemporaryFile(
        mode="w", suffix=".txt", delete=False
    ) as f:
        f.write("\n".join(trace_lines) + "\n")
        trace_path = f.name
    try:
        result = subprocess.run(
            [str(RUNNER), trace_path],
            capture_output=True, text=True, timeout=timeout,
        )
        if result.returncode != 0:
            pytest.fail(
                f"Runner failed (rc={result.returncode}):\n"
                f"stdout: {result.stdout[:500]}\n"
                f"stderr: {result.stderr[:500]}"
            )
        return json.loads(result.stdout)
    finally:
        os.unlink(trace_path)


@pytest.mark.strict_extracted
def test_runner_halt():
    """HALT instruction should produce mu = cost."""
    state = _run_trace(["HALT 5"])
    assert state["mu"] == 5, f"Expected mu=5, got {state['mu']}"
    assert state["pc"] == 1


@pytest.mark.strict_extracted
def test_runner_pnew_mu_conservation():
    """PNEW charges exactly its cost."""
    state = _run_trace(["PNEW {0} 3", "HALT 1"])
    assert state["mu"] == 4, f"Expected mu=4 (3+1), got {state['mu']}"


@pytest.mark.strict_extracted
def test_runner_load_imm_and_add():
    """LOAD_IMM + ADD computes correctly."""
    state = _run_trace([
        "LOAD_IMM 0 10 1",    # r0 = 10, cost=1
        "LOAD_IMM 1 20 1",    # r1 = 20, cost=1
        "ADD 2 0 1 1",        # r2 = r0+r1 = 30, cost=1
        "HALT 1",
    ])
    assert state["mu"] == 4
    assert state["regs"][2] == 30


@pytest.mark.strict_extracted
def test_runner_multi_pnew():
    """Multiple PNEW instructions accumulate mu."""
    state = _run_trace([
        "PNEW {0} 2",
        "PNEW {0} 2",
        "PNEW {0} 2",
        "HALT 1",
    ])
    assert state["mu"] == 7, f"Expected mu=7 (2+2+2+1), got {state['mu']}"


