(** Joint support of a bipartite state in finite coordinates (a,b). *)
Definition joint_support := list (nat * nat).

(** Boundary edges on a 1D cell lattice: an edge crosses the split when
    consecutive cells belong to opposite regions. *)
Definition cut_edge_1d (left right : list nat) (x : nat) : bool :=
  orb (andb (nat_list_mem x left) (nat_list_mem (S x) right))
      (andb (nat_list_mem x right) (nat_list_mem (S x) left)).

(** Boundary size of the split interface (1D discretization). *)
Definition boundary_size_1d (left right : list nat) : nat :=
  List.length (filter (cut_edge_1d left right)
    (normalize_region (left ++ right))).

(** Partial trace over the right subsystem at support level:
    project support onto left coordinates. *)
Definition partial_trace_right_support (support : joint_support) : list nat :=
  nodup Nat.eq_dec (map fst support).

(** Reduced support on subsystem A after tracing out B. *)
Definition reduced_state_support (support : joint_support) : list nat :=
  partial_trace_right_support support.

(** Locality condition used by area-law proofs. *)
Definition boundary_local_support
  (left right : list nat)
  (support : joint_support) : Prop :=
  List.length (reduced_state_support support) <=
  Nat.pow 2 (boundary_size_1d left right).

(** Split morphism semantics: explicit split regions + reachable joint supports. *)
Record SplitMorphism := {
  split_left : list nat;
  split_right : list nat;
  coupling_pairs : list (nat * nat);
  morphism_support_semantics : list joint_support;

  (** Every declared coupling connects left to right. *)
  coupling_pairs_wf :
    forall a b,
      In (a, b) coupling_pairs ->
      In a split_left /\ In b split_right;

  (** Reachable reduced-support modes are generated by declared couplings. *)
  support_generated_by_couplings :
    forall support,
      In support morphism_support_semantics ->
      forall a,
        In a (reduced_state_support support) ->
        exists b, In (a, b) coupling_pairs
}.

Definition coupled (P : SplitMorphism) (a b : nat) : Prop :=
  In (a, b) (coupling_pairs P).

(** Nearest-neighbor locality at morphism level:
    couplings only occur across cut-adjacent boundary cells. *)
Definition is_nearest_neighbor (P : SplitMorphism) : Prop :=
  forall a,
    In a (split_left P) ->
    forall b,
      In b (split_right P) ->
      coupled P a b ->
      cut_edge_1d (split_left P) (split_right P) a = true.

Lemma nodup_nat_NoDup :
  forall xs,
    NoDup (nodup Nat.eq_dec xs).
Proof.
  induction xs as [|x xs IH]; simpl.
  - constructor.
  - destruct (in_dec Nat.eq_dec x xs) as [Hin | Hnotin].
    + exact IH.
    + constructor.
      * intro Hcontra.
        apply nodup_In in Hcontra.
        contradiction.
      * exact IH.
Qed.

Lemma reduced_support_nodup :
  forall support,
    NoDup (reduced_state_support support).
Proof.
  intro support.
  unfold reduced_state_support, partial_trace_right_support.
  apply nodup_nat_NoDup.
Qed.

Lemma in_normalize_union_of_left :
  forall left right a,
    In a left ->
    In a (normalize_region (left ++ right)).
Proof.
  intros left right a Hin.
  unfold normalize_region.
  apply nodup_In.
  apply in_or_app. left. exact Hin.
Qed.

Lemma reduced_support_in_boundary_carrier :
  forall (P : SplitMorphism) (support : joint_support) (a : nat),
    is_nearest_neighbor P ->
    In support (morphism_support_semantics P) ->
    In a (reduced_state_support support) ->
    In a
      (filter (cut_edge_1d (split_left P) (split_right P))
        (normalize_region (split_left P ++ split_right P))).
Proof.
  intros P support a Hnn Hin_sem Hin_red.
  destruct (support_generated_by_couplings P support Hin_sem a Hin_red) as [b Hpair].
  pose proof (coupling_pairs_wf P a b Hpair) as [Hin_left Hin_right].
  assert (Hcut : cut_edge_1d (split_left P) (split_right P) a = true).
  { apply (Hnn a Hin_left b Hin_right). exact Hpair. }
  apply filter_In.
  split.
  - apply in_normalize_union_of_left. exact Hin_left.
  - exact Hcut.
Qed.

Lemma reduced_support_length_le_boundary_size :
  forall (P : SplitMorphism) (support : joint_support),
    is_nearest_neighbor P ->
    In support (morphism_support_semantics P) ->
    List.length (reduced_state_support support) <=
    boundary_size_1d (split_left P) (split_right P).
Proof.
  intros P support Hnn Hin_sem.
  unfold boundary_size_1d.
  apply NoDup_incl_length.
  - apply reduced_support_nodup.
  - intros a Ha.
    eapply reduced_support_in_boundary_carrier; eauto.
Qed.

Lemma pow2_ge_1 : forall n, 1 <= Nat.pow 2 n.
Proof.
  induction n as [|n IH].
  - simpl. lia.
  - simpl. lia.
Qed.

Lemma nat_le_pow2_self : forall n, n <= Nat.pow 2 n.
Proof.
  induction n as [|n IH].
  - simpl. lia.
  - change (S n <= Nat.pow 2 (S n)).
    simpl.
    assert (Hpow1 : 1 <= Nat.pow 2 n).
    { apply pow2_ge_1. }
    assert (Hsn : S n <= S (Nat.pow 2 n)).
    { lia. }
    assert (Hstep : S (Nat.pow 2 n) <= 2 * Nat.pow 2 n).
    { lia. }
    lia.
Qed.

