    PSPLIT-to-SplitMorphism bridge (execution-level connection)
    ------------------------------------------------------------------------ *)

Definition cartesian_pairs (xs ys : list nat) : list (nat * nat) :=
  flat_map (fun x => map (fun y => (x, y)) ys) xs.

Lemma in_map_fst_exists_pair :
  forall (pairs : list (nat * nat)) a,
    In a (map fst pairs) ->
    exists b, In (a, b) pairs.
Proof.
  induction pairs as [|[x y] rest IH]; intros a Hin; simpl in *.
  - contradiction.
  - destruct Hin as [Heq | Hin_rest].
    + subst a. exists y. left. reflexivity.
    + apply IH in Hin_rest.
      destruct Hin_rest as [b Hb].
      exists b. right. exact Hb.
Qed.

Lemma in_cartesian_pairs_wf :
  forall xs ys a b,
    In (a, b) (cartesian_pairs xs ys) ->
    In a xs /\ In b ys.
Proof.
  intros xs ys a b Hin.
  unfold cartesian_pairs in Hin.
  apply in_flat_map in Hin.
  destruct Hin as [x [Hx Hmap]].
  apply in_map_iff in Hmap.
  destruct Hmap as [y [Heq Hy]].
  inversion Heq; subst.
  split; assumption.
Qed.

Definition psplit_support_morphism (left right : list nat) : SplitMorphism.
Proof.
  refine
    {| split_left := normalize_region left;
       split_right := normalize_region right;
       coupling_pairs := cartesian_pairs (normalize_region left) (normalize_region right);
       morphism_support_semantics :=
         [cartesian_pairs (normalize_region left) (normalize_region right)] |}.
  - intros a b Hin.
    apply in_cartesian_pairs_wf in Hin.
    exact Hin.
  - intros support Hin_sem a Hin_red.
    simpl in Hin_sem.
    destruct Hin_sem as [Hsupport | Hnil].
    + subst support.
      unfold reduced_state_support, partial_trace_right_support in Hin_red.
      apply nodup_In in Hin_red.
      apply in_map_fst_exists_pair in Hin_red.
      exact Hin_red.
    + contradiction.
Defined.

Theorem vm_step_psplit_constructs_support_morphism :
  forall s s' module left right cost,
    vm_step s (instr_psplit module left right cost) s' ->
    exists P : SplitMorphism,
      split_left P = normalize_region left /\
      split_right P = normalize_region right /\
      In (cartesian_pairs (normalize_region left) (normalize_region right))
         (morphism_support_semantics P) /\
      (forall support,
        In support (morphism_support_semantics P) ->
        forall a,
          In a (reduced_state_support support) ->
          exists b, coupled P a b).
Proof.
  intros s s' module left right cost Hstep.
  inversion Hstep; subst.
  - exists (psplit_support_morphism left right).
    split.
    + reflexivity.
    + split.
      * reflexivity.
      * split.
        -- simpl. left. reflexivity.
        -- intros support Hin_sem a Hin_red.
           simpl in Hin_sem.
           destruct Hin_sem as [Hsupport | Hnil].
           ++ subst support.
              unfold reduced_state_support, partial_trace_right_support in Hin_red.
              apply nodup_In in Hin_red.
              apply in_map_fst_exists_pair in Hin_red.
              destruct Hin_red as [b Hb].
              exists b. unfold coupled. exact Hb.
           ++ contradiction.
  - exists (psplit_support_morphism left right).
    split.
    + reflexivity.
    + split.
      * reflexivity.
      * split.
        -- simpl. left. reflexivity.
        -- intros support Hin_sem a Hin_red.
           simpl in Hin_sem.
           destruct Hin_sem as [Hsupport | Hnil].
           ++ subst support.
              unfold reduced_state_support, partial_trace_right_support in Hin_red.
              apply nodup_In in Hin_red.
              apply in_map_fst_exists_pair in Hin_red.
              destruct Hin_red as [b Hb].
              exists b. unfold coupled. exact Hb.
           ++ contradiction.
Qed.

(** Reachable support model induced by concrete vm_apply on PSPLIT. *)
Definition vm_apply_psplit_reachable_supports
  (s : VMState)
  (module : ModuleID)
  (left right : list nat)
  (cost : nat) : list joint_support :=
  match graph_psplit s.(vm_graph) module left right with
  | Some _ => [cartesian_pairs (normalize_region left) (normalize_region right)]
  | None => []
  end.

(** Conservativity (successful PSPLIT case): every support in the cartesian
    split semantics is reachable in the vm_apply-induced support model. *)
Theorem psplit_cartesian_semantics_conservative_vm_apply :
  forall s s' module left right cost,
    vm_step s (instr_psplit module left right cost) s' ->
    graph_psplit s.(vm_graph) module left right <> None ->
    forall support,
      In support
        (morphism_support_semantics (psplit_support_morphism left right)) ->
      In support (vm_apply_psplit_reachable_supports s module left right cost).
Proof.
  intros s s' module left right cost _ Hsucc support Hin.
  unfold vm_apply_psplit_reachable_supports.
  destruct (graph_psplit s.(vm_graph) module left right) as [[[g' l] r]|] eqn:Hsplit.
  - simpl in *. exact Hin.
  - exfalso. apply Hsucc. reflexivity.
Qed.

(** vm_apply agreement for successful PSPLIT vm_step transitions. *)
Theorem psplit_vm_apply_reaches_step_state :
  forall s s' module left right cost,
    vm_step s (instr_psplit module left right cost) s' ->
    graph_psplit s.(vm_graph) module left right <> None ->
    vm_apply s (instr_psplit module left right cost) = s'.
Proof.
  intros s s' module left right cost Hstep _.
  apply vm_step_vm_apply. exact Hstep.
Qed.

Theorem local_morphism_support_bound :
  forall (P : SplitMorphism) (support : joint_support),
    is_nearest_neighbor P ->
    In support (morphism_support_semantics P) ->
    boundary_local_support (split_left P) (split_right P) support.
Proof.
  intros P support Hnn Hin.
  unfold boundary_local_support.
  eapply Nat.le_trans.
  - apply reduced_support_length_le_boundary_size; eauto.
  - apply nat_le_pow2_self.
Qed.
